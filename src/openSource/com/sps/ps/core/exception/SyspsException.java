package com.sps.ps.core.exception;

/***
 * 定义系统异常类, 如果要自定义异常类，可以都继承本类
 * @author 970815940@qq.com
 * 2013-09-23 14:55:03
 *
 */
public class SyspsException extends RuntimeException {

	/**
	 * 
	 */
	private static final long serialVersionUID = 1L;
	/**
	 * 静止空异常信息的异常
	 */
	private SyspsException(){
		
	}
	public SyspsException(String Msg){
		super(Msg);
	}
	public SyspsException(String Msg,Throwable exception){
		super(Msg,exception);
	}
	public SyspsException(Throwable exception){
		super(exception);
		
	}
	
}
